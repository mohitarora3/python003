def sumList(list):
    '''
    objective: to compute sum of list
    input parameters:
                  list: consist of elemnts of which sum has to be found
    return value: sum of elements of list
    '''
    #approach: using recursion 
    
    if list == []:
        return 0
    else:
        return(list[0]+sumList(list[1:]))
        
print(sumList([1,2,3]))
        
